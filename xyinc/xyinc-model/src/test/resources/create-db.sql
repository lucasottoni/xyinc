CREATE TABLE model (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
	name VARCHAR(30) NOT NULL UNIQUE,
	PRIMARY KEY (id)
);

CREATE TABLE model_field (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
	id_model INTEGER NOT NULL,
	name VARCHAR(30) NOT NULL,
	type VARCHAR(20) NOT NULL,
	PRIMARY KEY (id)
);

ALTER TABLE model_field ADD CONSTRAINT fk_model FOREIGN KEY (id_model) REFERENCES model(id);
ALTER TABLE model_field ADD CONSTRAINT un_name_model_field UNIQUE (id_model, name);