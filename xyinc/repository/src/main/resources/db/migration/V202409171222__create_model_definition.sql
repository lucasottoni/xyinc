CREATE TABLE ${app_schema}.MODEL_DEFINITION (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    fields jsonb NOT NULL
);

CREATE TABLE ${app_schema}.MODEL_DEFINITION_AUD (
    id BIGINT,
    name VARCHAR(255) NOT NULL,
    fields jsonb NOT NULL,
    rev BIGINT PRIMARY KEY REFERENCES ${app_schema}.REVINFO(REV),
    revtype SMALLINT
);
